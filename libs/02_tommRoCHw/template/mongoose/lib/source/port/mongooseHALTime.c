double mg_time(void) {
    return (double) mg_millis() / 1000.0f;
}

void mg_usleep(unsigned long usecs) {
    TOMMROC_ASSERT_TOMMROC_LIB_FUNCT_SUCCESS(tommRoCOsDelayMsec(TOMMROC_UNIT_MEASURE_TIME_APPROX_uSEC_TO_mSEC(usecs)));
}

unsigned long mg_millis(void) {
    return tommRoCOsUptime();
}

