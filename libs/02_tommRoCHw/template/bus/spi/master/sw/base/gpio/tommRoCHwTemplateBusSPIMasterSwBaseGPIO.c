void tommRoCHwTemplateBusSPIMasterSwBaseGPIOOutputBit(
        const   uint8_t                     data,
        const   uint8_t                     mask,
        const   tommRoC_hw_gpio_set_cb_t    mosiHigh,
        const   tommRoC_hw_gpio_set_cb_t    mosiLow) {

    if (data & mask) {
        TOMMROC_HW_ASSERT_TOMMROC_HW_LIB_FUNCT_SUCCESS(mosiHigh());
    }
    else {
        TOMMROC_HW_ASSERT_TOMMROC_HW_LIB_FUNCT_SUCCESS(mosiLow());
    }
}
